package com.mytrynohelp;

public class Ten_To_One_Even {
	//print from 10 to 1 but only even numbers
	public static void main(String[] args) {
		
		
		for (int i = 10; i>=1; i--) {
			
			if (i%2==0) {
				System.out.print(i+"  ");
				
		//(%)this symbol is to find reminder 		
				
			}
		}
		
		
		
	}

}
